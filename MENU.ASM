DATA SEGMENT
MENU1 DB "ENTER YOUR CHOICE",13,10
      DB "1.ADDITION",13,10
      DB "2.SUBTRACTION",13,10
      DB "3.EXIT$",13,10

CONT DB "DO YOU WISH TO CONTINUE?(Y/N) $",13,10

NUM1 DB "ENTER FIRST NUMBER$",13,10

NUM2 DB "ENTER SECOND NUMBER$",13,10

OP1 DB ?
OP2 DB ?


RES DB "RESULT IS   $",13,10

MSG DB "GOODBYEE$",13,10

DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
       MOV DS,AX

AGAIN:CALL CLEAR_SCREEN

       LEA DX,MENU1
       MOV AH,09H
       INT 21H
       CALL NEXT_LINE
       MOV AH,01H
       INT 21H

       CMP AL,31H
       JE ADD1

       CMP AL,32H
       JE SUB1

       CMP AL,33H
       JE EXIT

ADD1:CALL NEXT_LINE
     CALL DISPLAY_MESSAGE
     LEA DX,RES
     MOV AH,09H
     INT 21H
     MOV AL,OP1
     MOV BL,OP2
     ADD AL,BL
     AAA
     OR AX,3030H
     MOV AH,0EH
     INT 10H
     CALL NEXT_LINE
     LEA DX,CONT
     MOV AH,09
     INT 21H
     MOV AH,01H
     INT 21H
     CMP AL,59H
     JE AGAIN
     JMP EXIT

TEMP:JMP AGAIN
SUB1:CALL NEXT_LINE
     CALL DISPLAY_MESSAGE
     CALL NEXT_LINE
     LEA DX,RES
     MOV AH,09H
     INT 21H
     MOV AL,OP1
     MOV BL,OP2
     SUB AL,BL
     AAS
     OR AX,3030H
     MOV AH,0EH
     INT 10H
     CALL NEXT_LINE
     LEA DX,CONT
     MOV AH,09
     INT 21H
     MOV AH,01H
     INT 21H
     CMP AL,59H
     JE TEMP
     

EXIT:CALL NEXT_LINE
     LEA DX,MSG
     MOV AH,09H
     INT 21H
     JMP EXIT2

CLEAR_SCREEN PROC
     MOV AH,00H
     MOV AL,03H
     INT 10H
     RET
CLEAR_SCREEN ENDP

DISPLAY_MESSAGE PROC
      CALL NEXT_LINE
      LEA DX,NUM1
      MOV AH,09H
      INT 21H
      MOV AH,01H
      INT 21H
      MOV OP1,AL
      CALL NEXT_LINE
      LEA DX,NUM2
      MOV AH,09
      INT 21H
      MOV AH,01H
      INT 21H
      MOV OP2,AL
      RET
DISPLAY_MESSAGE ENDP

NEXT_LINE PROC
      MOV AH,02H
      MOV DL,0DH
      INT 21H
      MOV DL,0AH
      INT 21H
      RET
NEXT_LINE ENDP

EXIT2:MOV AH,4CH
      INT 21H
      CODE ENDS
      END START
